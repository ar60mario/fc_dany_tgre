    private boolean verificarDuplicado() {
        Boolean libre = true;
        if (!codigoTxt.getText().isEmpty()) {
            int cod = Integer.valueOf(codigoTxt.getText());
            Proveedor2 p = null;
            try {
                p = new Proveedor2Service().getProveedorByCodigo(cod);
                if (p != null) {
                    libre = false;
                }
            } catch (Exception ex) {
//                Logger.getLogger(ModificarProveedorFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (libre) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "N√∫mero Proveedor existente");
            return false;
        }
    }
