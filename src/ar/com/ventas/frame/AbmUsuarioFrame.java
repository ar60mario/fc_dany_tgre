    private void modificar() {
        int row = tabla.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "ERROR");
            return;
        }
        Usuario us = usuarios.get(row);
        ModificarUsuarioFrame muf = new ModificarUsuarioFrame(us);
        muf.setVisible(true);
        this.dispose();
    }

